#include <iostream>

using namespace std;

int addTwoNumbers(int num1, int num2) {
	int sum = num1 + num2;
	return sum;
}

int multTwoNumbers(int num1, int num2) {
	int sum = num1 * num2;
	return sum;
}

int main() {
	
	cout << addTwoNumbers(10, 30)<<endl;
	cout << multTwoNumbers(10, 30);
}

